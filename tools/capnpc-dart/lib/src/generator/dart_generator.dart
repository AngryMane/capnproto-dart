void _writeInterface(
  StringBuffer sb,
  SchemaNode node,
  InterfaceBody body,
  Map<int, SchemaNode> nodeMap,
) {
  final name = _dartClassName(node.displayName);

  // Collect all methods including inherited ones (depth-first from ancestors).
  final rawMethods = <({SchemaNode ifaceNode, SchemaMethod method})>[];
  _collectAllMethods(node, body, nodeMap, rawMethods, {});

  // Detect name conflicts: a method name is conflicted when it appears in 2+
  // *different* interface nodes.  Same-interface names are impossible (Cap'n
  // Proto schemas forbid duplicate method names per interface).
  final nameToIfaceIds = <String, Set<int>>{};
  for (final m in rawMethods) {
    (nameToIfaceIds[_camel(m.method.name)] ??= {}).add(m.ifaceNode.id);
  }
  final conflictedNames = {
    for (final e in nameToIfaceIds.entries)
      if (e.value.length > 1) e.key,
  };

  // Resolve the Dart-level method name (prefixed when there is a conflict).
  String resolveDartName(SchemaNode ifaceNode, SchemaMethod method) {
    final camel = _camel(method.name);
    if (!conflictedNames.contains(camel)) return camel;
    final shortIface = _dartClassName(ifaceNode.displayName);
    return '${_lcfirst(shortIface)}${_ucfirst(camel)}';
  }

  final allMethods = rawMethods
      .map((m) => (
            ifaceNode: m.ifaceNode,
            method: m.method,
            dartName: resolveDartName(m.ifaceNode, m.method),
          ))
      .toList();

  // ---- Pipeline classes (one per method whose result has interface fields) ----
  for (final m in allMethods) {
    _writeResultsPipeline(sb, name, m.method, nodeMap, m.dartName);
  }

  // ---- Client stub ----
  sb.writeln('class ${name}Client extends Capability {');
  sb.writeln('  static const int _interfaceId = ${_hexId(node.id)};');
  sb.writeln();
  sb.writeln('  final Capability _cap;');
  sb.writeln('  ${name}Client(this._cap);');

  for (final m in allMethods) {
    _writeClientMethod(sb, name, m.ifaceNode, m.method, nodeMap, m.dartName);
  }

  sb.writeln();
  sb.writeln('  @override');
  sb.writeln('  Future<void> dispose() => _cap.dispose();');
  sb.writeln('}');
  sb.writeln();

  // ---- Factory ----
  sb.writeln(
      'class ${name}ClientFactory extends CapabilityFactory<${name}Client> {');
  sb.writeln('  @override');
  sb.writeln(
      '  ${name}Client fromCapability(Capability cap) => ${name}Client(cap);');
  sb.writeln('}');
  sb.writeln();

  // ---- Server stub ----
  _writeServerStub(sb, node, allMethods, nodeMap);
}

/// Depth-first collection of all methods reachable via inheritance.
/// Each method retains its defining interface node; the wire-level method ID
/// is carried in `method.ordinal`.
void _collectAllMethods(
  SchemaNode ifaceNode,
  InterfaceBody body,
  Map<int, SchemaNode> nodeMap,
  List<({SchemaNode ifaceNode, SchemaMethod method})> result,
  Set<int> seen,
) {
  if (seen.contains(ifaceNode.id)) return;
  seen.add(ifaceNode.id);

  for (final scId in body.superclassIds) {
    final scNode = nodeMap[scId];
    if (scNode == null || scNode.body is! InterfaceBody) continue;
    _collectAllMethods(
        scNode, scNode.body as InterfaceBody, nodeMap, result, seen);
  }

  for (final method in body.methods) {
    result.add((ifaceNode: ifaceNode, method: method));
  }
}

void _writeClientMethod(
  StringBuffer sb,
  String ifaceName,
  SchemaNode ifaceNode,
  SchemaMethod method,
  Map<int, SchemaNode> nodeMap,
  String dartName,
) {
  final methodName = dartName;
  final paramsNode = nodeMap[method.paramStructTypeId];
  final resultsNode = nodeMap[method.resultStructTypeId];
  final paramsName = _dartClassName(paramsNode?.displayName ?? 'Unknown');
  final resultsName = _dartClassName(resultsNode?.displayName ?? 'Unknown');
  final ifaceId = _hexId(ifaceNode.id);
  final ordinal = method.ordinal;

  final capParams = _collectCapParams(paramsNode);
  final capResults = _collectCapResults(resultsNode);
  final hasPipeline = capResults.isNotEmpty;

  final namedParams = capParams.isEmpty
      ? ''
      : ', {${capParams.map((p) => 'required Capability ${p.$2}').join(', ')}}';
  final capsList = capParams.map((p) => p.$2).join(', ');
  final dispatchCaps =
      capParams.isEmpty ? '' : ', paramsCapabilities: [$capsList]';

  sb.writeln();

  // ---- Normal async method (always returns Future<XxxResultsReader>) ----
  sb.writeln(
      '  Future<${resultsName}Reader> $methodName(void Function(${paramsName}Builder) build$namedParams) async {');
  sb.writeln('    final mb = MessageBuilder();');
  if (capParams.isEmpty) {
    sb.writeln('    build(mb.initRoot(${_lcfirst(paramsName)}Factory));');
  } else {
    sb.writeln('    final b = mb.initRoot(${_lcfirst(paramsName)}Factory);');
    for (final (_, fname, capIdx) in capParams) {
      sb.writeln('    b.set${_ucfirst(fname)}($capIdx);');
    }
    sb.writeln('    build(b);');
  }
  sb.writeln(
      '    final result = await _cap.dispatch($ifaceId, $ordinal, mb.serialize()$dispatchCaps);');
  sb.writeln(
      '    return MessageReader.deserialize(result.bytes).getRoot(${_lcfirst(resultsName)}Factory);');
  sb.writeln('  }');

  // ---- Pipeline method (only when result has interface fields) ----
  if (hasPipeline) {
    final pipelineName = '${ifaceName}${_ucfirst(methodName)}Pipeline';
    sb.writeln();
    sb.writeln(
        '  $pipelineName ${methodName}Pipeline(void Function(${paramsName}Builder) build$namedParams) {');
    sb.writeln('    final mb = MessageBuilder();');
    if (capParams.isEmpty) {
      sb.writeln('    build(mb.initRoot(${_lcfirst(paramsName)}Factory));');
    } else {
      sb.writeln('    final b = mb.initRoot(${_lcfirst(paramsName)}Factory);');
      for (final (_, fname, capIdx) in capParams) {
        sb.writeln('    b.set${_ucfirst(fname)}($capIdx);');
      }
      sb.writeln('    build(b);');
    }
    sb.writeln(
        '    return $pipelineName._(_cap.beginDispatch($ifaceId, $ordinal, mb.serialize()$dispatchCaps));');
    sb.writeln('  }');
  }
}

/// Returns the [InterfaceRefType] fields (sorted by codeOrder) in [paramsNode]
/// as a list of `(ptrOffset, fieldName, capTableIndex)`.
List<(int, String, int)> _collectCapParams(SchemaNode? paramsNode) {
  if (paramsNode == null) return const [];
  final body = paramsNode.body;
  if (body is! StructBody) return const [];

  final fields = [...body.fields]
    ..sort((a, b) => a.codeOrder.compareTo(b.codeOrder));
  final result = <(int, String, int)>[];
  int capIdx = 0;
  for (final field in fields) {
    if (field.body is! SlotField) continue;
    final sf = field.body as SlotField;
    if (sf.type is InterfaceRefType) {
      result.add((sf.offset, _camel(field.name), capIdx++));
    }
  }
  return result;
}

/// Returns true if [resultsNode] represents an effectively-void result struct.
bool _isVoidResultStruct(SchemaNode? resultsNode) {
  if (resultsNode == null) return true;
  final body = resultsNode.body;
  if (body is! StructBody) return true;
  if (body.dataWordCount == 0 && body.pointerCount == 0) return true;
  return body.fields.every((f) {
    if (f.body is! SlotField) return false;
    return (f.body as SlotField).type is VoidType;
  });
}

void _writeServerStub(
  StringBuffer sb,
  SchemaNode node,
  List<({SchemaNode ifaceNode, SchemaMethod method, String dartName})> allMethods,
  Map<int, SchemaNode> nodeMap,
) {
  final name = _dartClassName(node.displayName);

  sb.writeln('abstract class ${name}Server extends Capability {');

  for (final m in allMethods) {
    final method = m.method;
    final paramsNode = nodeMap[method.paramStructTypeId];
    final resultsNode = nodeMap[method.resultStructTypeId];
    final methodName = m.dartName;
    final paramsName = _dartClassName(paramsNode?.displayName ?? 'Unknown');
    final isVoid = _isVoidResultStruct(resultsNode);

    sb.writeln();
    if (isVoid) {
      sb.writeln(
          '  Future<void> $methodName(${paramsName}Reader params, List<Capability> paramsCapabilities);');
    } else {
      sb.writeln(
          '  Future<DispatchResult> $methodName(${paramsName}Reader params, List<Capability> paramsCapabilities);');
    }
  }

  sb.writeln();
  sb.writeln('  @override');
  sb.writeln(
      '  Future<DispatchResult> dispatch(int interfaceId, int methodId, Uint8List params, {');
  sb.writeln('    List<Capability> paramsCapabilities = const [],');
  sb.writeln('  }) async {');

  if (allMethods.isNotEmpty) {
    // Group by defining interface ID.
    final byIface =
        <int, List<({SchemaNode ifaceNode, SchemaMethod method, String dartName})>>{};
    for (final m in allMethods) {
      (byIface[m.ifaceNode.id] ??= []).add(m);
    }

    sb.writeln('    switch (interfaceId) {');
    for (final entry in byIface.entries) {
      sb.writeln('      case ${_hexId(entry.key)}:');
      sb.writeln('        switch (methodId) {');
      for (final m in entry.value) {
        final paramsNode = nodeMap[m.method.paramStructTypeId];
        final resultsNode = nodeMap[m.method.resultStructTypeId];
        final methodName = m.dartName;
        final paramsName =
            _dartClassName(paramsNode?.displayName ?? 'Unknown');
        final isVoid = _isVoidResultStruct(resultsNode);

        sb.writeln('          case ${m.method.ordinal}:');
        sb.writeln('            final p = MessageReader.deserialize(params)');
        sb.writeln(
            '                .getRoot(${_lcfirst(paramsName)}Factory);');
        if (isVoid) {
          sb.writeln('            await $methodName(p, paramsCapabilities);');
          sb.writeln('            return DispatchResult.empty;');
        } else {
          sb.writeln(
              '            return await $methodName(p, paramsCapabilities);');
        }
      }
      sb.writeln('          default:');
      sb.writeln('            break;');
      sb.writeln('        }');
    }
    sb.writeln('      default:');
    sb.writeln('        break;');
    sb.writeln('    }');
  }
  sb.writeln(
      '    return super.dispatch(interfaceId, methodId, params, paramsCapabilities: paramsCapabilities);');
  sb.writeln('  }');
  sb.writeln();
  sb.writeln('  @override');
  sb.writeln('  Future<void> dispose() async {}');
  sb.writeln('}');
}

/// Returns all interface fields (sorted by codeOrder) in [resultsNode]
/// as a list of `(ptrOffset, fieldName, typeId)`.
List<(int, String, int)> _collectCapResults(SchemaNode? resultsNode) {
  if (resultsNode == null) return const [];
  final body = resultsNode.body;
  if (body is! StructBody) return const [];
  final fields = [...body.fields]
    ..sort((a, b) => a.codeOrder.compareTo(b.codeOrder));
  final result = <(int, String, int)>[];
  for (final field in fields) {
    if (field.body is! SlotField) continue;
    final sf = field.body as SlotField;
    if (sf.type is InterfaceRefType) {
      result.add((sf.offset, _camel(field.name), (sf.type as InterfaceRefType).typeId));
    }
  }
  return result;
}

/// Emits an `XxxPipeline` class for [method] if its result struct has any
/// interface fields.  Each interface field becomes a typed `XxxClient` getter
/// using the correct pointer slot index (fixing the hardcoded-0 bug).
void _writeResultsPipeline(
  StringBuffer sb,
  String ifaceName,
  SchemaMethod method,
  Map<int, SchemaNode> nodeMap,
  String dartName,
) {
  final resultsNode = nodeMap[method.resultStructTypeId];
  final capResults = _collectCapResults(resultsNode);
  if (capResults.isEmpty) return;

  final methodName = dartName;
  final resultsName = _dartClassName(resultsNode?.displayName ?? 'Unknown');
  final pipelineName = '${ifaceName}${_ucfirst(methodName)}Pipeline';

  sb.writeln();
  sb.writeln('final class $pipelineName {');
  sb.writeln('  $pipelineName._(CapCall call)');
  final fieldInits = capResults.map((r) {
    final capIfaceName =
        _dartClassName(nodeMap[r.$3]?.displayName ?? 'Unknown');
    return '    ${r.$2} = ${capIfaceName}Client(call.pipelineResult(${r.$1}))';
  }).join(',\n');
  final resultInit =
      '    result = call.result.then((r) => MessageReader.deserialize(r.bytes).getRoot(${_lcfirst(resultsName)}Factory))';
  sb.writeln('      : $fieldInits,\n$resultInit;');
  sb.writeln();
  sb.writeln('  final Future<${resultsName}Reader> result;');
  for (final (_, fname, typeId) in capResults) {
    final capIfaceName =
        _dartClassName(nodeMap[typeId]?.displayName ?? 'Unknown');
    sb.writeln('  final ${capIfaceName}Client $fname;');
  }
  sb.writeln('}');
}

