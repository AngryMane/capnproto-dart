// Struct generation
// ---------------------------------------------------------------------------

void _writeStruct(
  StringBuffer sb,
  SchemaNode node,
  StructBody body,
  Map<int, SchemaNode> nodeMap,
) {
  final name = _dartClassName(node.displayName);
  final dataWords = body.dataWordCount;
  final ptrWords = body.pointerCount;

  final fields = [...body.fields]
    ..sort((a, b) => a.codeOrder.compareTo(b.codeOrder));

  // ---- Reader ----
  sb.writeln('final class ${name}Reader extends StructReader {');
  sb.writeln('  ${name}Reader(super.raw);');

  if (body.discriminantCount > 0) {
    final discByteOffset = body.discriminantOffset * 2;
    sb.writeln();
    sb.writeln('  int get which => getUint16Field($discByteOffset);');
  }

  for (final field in fields) {
    _writeReaderField(sb, field, nodeMap);
  }
  sb.writeln('}');
  sb.writeln();

  // ---- Builder ----
  sb.writeln('final class ${name}Builder extends StructBuilder {');
  sb.writeln('  ${name}Builder(super.raw);');
  sb.writeln();
  sb.writeln('  @override');
  sb.writeln('  ${name}Reader asReader() => ${name}Reader(rawToReader());');

  if (body.discriminantCount > 0) {
    final discByteOffset = body.discriminantOffset * 2;
    sb.writeln();
    sb.writeln('  void _setWhich(int v) => setUint16Field($discByteOffset, v);');
  }

  for (final field in fields) {
    _writeBuilderField(sb, field, nodeMap, body);
  }
  sb.writeln('}');
  sb.writeln();

  // ---- Factory ----
  sb.writeln('final class _${name}Factory');
  sb.writeln('    extends StructFactory<${name}Reader, ${name}Builder> {');
  sb.writeln('  @override int get dataWords => $dataWords;');
  sb.writeln('  @override int get ptrWords => $ptrWords;');
  sb.writeln('  @override');
  sb.writeln(
      '  ${name}Reader fromRawReader(RawStructReader r) => ${name}Reader(r);');
  sb.writeln('  @override');
  sb.writeln(
      '  ${name}Builder fromRawBuilder(RawStructBuilder r) => ${name}Builder(r);');
  sb.writeln('}');
  sb.writeln();
  sb.writeln('final ${_lcfirst(name)}Factory = _${name}Factory();');
}

void _writeReaderField(
  StringBuffer sb,
  SchemaField field,
  Map<int, SchemaNode> nodeMap,
) {
  final fname = _camel(field.name);
  sb.writeln();

  if (field.body is GroupField) {
    final gf = field.body as GroupField;
    final groupNode = nodeMap[gf.typeId];
    if (groupNode == null) return;
    final gname = _dartClassName(groupNode.displayName);
    sb.writeln('  ${gname}Reader get $fname => ${gname}Reader(raw);');
    return;
  }

  final sf = field.body as SlotField;
  final type = sf.type;
  final offset = sf.offset;

  // Void fields carry no data; reading them is meaningless, so skip.
  if (type is VoidType) return;

  // Capability pointer fields expose the cap-table index so callers can
  // look up the capability from DispatchResult.caps.
  if (type is InterfaceRefType) {
    sb.writeln(
        '  int get ${fname}CapIndex => getCapabilityField($offset);');
    return;
  }

  final (dartType, getter) =
      _readerGetter(type, offset, nodeMap, sf.defaultValue);
  sb.writeln('  $dartType get $fname => $getter;');
}

void _writeBuilderField(
  StringBuffer sb,
  SchemaField field,
  Map<int, SchemaNode> nodeMap,
  StructBody structBody,
) {
  final fname = _camel(field.name);
  final discVal = field.discriminantValue;
  final hasDisc = field.isUnionField;
  final discByteOffset = structBody.discriminantOffset * 2;

  if (field.body is GroupField) {
    final gf = field.body as GroupField;
    final groupNode = nodeMap[gf.typeId];
    if (groupNode == null) return;
    final gname = _dartClassName(groupNode.displayName);
    sb.writeln();
    if (hasDisc) {
      sb.writeln('  ${gname}Builder get $fname {');
      sb.writeln('    setUint16Field($discByteOffset, $discVal);');
      sb.writeln('    return ${gname}Builder(raw);');
      sb.writeln('  }');
    } else {
      sb.writeln('  ${gname}Builder get $fname => ${gname}Builder(raw);');
    }
    return;
  }

  final sf = field.body as SlotField;
  final type = sf.type;
  final offset = sf.offset;

  // Void field: no setter is meaningful.
  // For a union member emit a selection method so callers can set the
  // discriminant; for non-union Void fields emit nothing at all.
  if (type is VoidType) {
    if (hasDisc) {
      sb.writeln();
      sb.writeln('  void select${_ucfirst(fname)}() {');
      sb.writeln('    setUint16Field($discByteOffset, $discVal);');
      sb.writeln('  }');
    }
    return;
  }

  sb.writeln();

  if (_isPointerType(type)) {
    _writeBuilderPointerField(sb, fname, type, offset, nodeMap, hasDisc,
        discVal, discByteOffset);
  } else {
    _writeBuilderDataField(sb, fname, type, offset, nodeMap, hasDisc, discVal,
        discByteOffset, sf.defaultValue);
  }
}

void _writeBuilderDataField(
  StringBuffer sb,
  String fname,
  SchemaType type,
  int offset,
  Map<int, SchemaNode> nodeMap,
  bool hasDisc,
  int discVal,
  int discByteOffset, [
  Object? defaultValue,
]) {
  final (dartType, setter) = _builderDataSetter(type, offset, nodeMap, defaultValue);
  sb.writeln('  set $fname($dartType v) {');
  if (hasDisc) {
    sb.writeln('    setUint16Field($discByteOffset, $discVal);');
  }
  sb.writeln('    $setter;');
  sb.writeln('  }');
}

void _writeBuilderPointerField(
  StringBuffer sb,
  String fname,
  SchemaType type,
  int offset,
  Map<int, SchemaNode> nodeMap,
  bool hasDisc,
  int discVal,
  int discByteOffset,
) {
  final setDisc =
      hasDisc ? '    setUint16Field($discByteOffset, $discVal);\n' : '';

  if (type is TextType) {
    sb.writeln('  set $fname(String? v) {');
    if (hasDisc) sb.write(setDisc);
    sb.writeln('    setTextField($offset, v);');
    sb.writeln('  }');
  } else if (type is DataType) {
    sb.writeln('  set $fname(Uint8List? v) {');
    if (hasDisc) sb.write(setDisc);
    sb.writeln('    setDataField($offset, v);');
    sb.writeln('  }');
  } else if (type is StructRefType) {
    final refNode = nodeMap[type.typeId];
    final refName = _dartClassName(refNode?.displayName ?? 'Unknown');
    final refBody = refNode?.body;
    final dw = refBody is StructBody ? refBody.dataWordCount : 0;
    final pw = refBody is StructBody ? refBody.pointerCount : 0;
    final ucfname = _ucfirst(fname);
    sb.writeln('  ${refName}Builder init$ucfname() {');
    if (hasDisc) sb.write(setDisc);
    sb.writeln('    return initStructFieldWith($offset,');
    sb.writeln('        (r) => ${refName}Builder(r), $dw, $pw);');
    sb.writeln('  }');
    sb.writeln();
    sb.writeln('  bool has$ucfname() => hasPointerField($offset);');
  } else if (type is ListType) {
    _writeListBuilderField(
        sb, fname, type, offset, nodeMap, setDisc, hasDisc);
  } else if (type is AnyPointerType || type is TypeParameterRefType) {
    sb.writeln('  set $fname(Uint8List? v) {');
    if (hasDisc) sb.write(setDisc);
    sb.writeln('    if (v != null) setAnyPointerFromMessage($offset, v);');
    sb.writeln('  }');
  } else if (type is InterfaceRefType) {
    final ucfname = _ucfirst(fname);
    sb.writeln('  void set$ucfname(int capTableIndex) {');
    if (hasDisc) sb.write(setDisc);
    sb.writeln('    setCapabilityField($offset, capTableIndex);');
    sb.writeln('  }');
  }
}

void _writeListBuilderField(
  StringBuffer sb,
  String fname,
  ListType type,
  int offset,
  Map<int, SchemaNode> nodeMap,
  String setDisc,
  bool hasDisc,
) {
  final ucfname = _ucfirst(fname);
  final elem = type.elementType;

  final (initCall, retType) = _listInitCall(elem, offset, nodeMap);
  sb.writeln('  $retType init$ucfname(int length) {');
  if (hasDisc) sb.write(setDisc);
  sb.writeln('    return $initCall;');
  sb.writeln('  }');
}

