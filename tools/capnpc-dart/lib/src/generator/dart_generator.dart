import '../schema/schema_model.dart';

/// Generates a single Dart source file from a set of [SchemaNode]s that
/// belong to a given file node.
String generateDartFile(
  SchemaNode fileNode,
  List<SchemaNode> allNodes, {
  String packageName = 'capnproto_dart',
}) {
  final nodeMap = {for (final n in allNodes) n.id: n};
  final sb = StringBuffer();

  final structs = <SchemaNode>[];
  final enums = <SchemaNode>[];
  final interfaces = <SchemaNode>[];
  _collectNodes(fileNode, nodeMap, structs, enums, interfaces);

  sb.writeln('// Generated by capnpc-dart — do not edit by hand.');
  sb.writeln('// ignore_for_file: annotate_overrides, unnecessary_this');
  sb.writeln();
  sb.writeln("import 'dart:typed_data';");
  sb.writeln("import 'package:$packageName/$packageName.dart';");
  if (interfaces.isNotEmpty) {
    sb.writeln("import 'package:capnproto_dart_rpc/capnproto_dart_rpc.dart';");
  }
  sb.writeln();

  for (final node in enums) {
    _writeEnum(sb, node, node.body as EnumBody);
    sb.writeln();
  }

  for (final node in structs) {
    _writeStruct(sb, node, node.body as StructBody, nodeMap);
    sb.writeln();
  }

  final groups = <SchemaNode>[];
  final groupSeen = <int>{};
  for (final node in structs) {
    _collectGroups(node, nodeMap, groups, groupSeen);
  }
  for (final node in groups) {
    _writeStruct(sb, node, node.body as StructBody, nodeMap);
    sb.writeln();
  }

  for (final node in interfaces) {
    _writeInterface(sb, node, node.body as InterfaceBody, nodeMap);
    sb.writeln();
  }

  return sb.toString();
}

void _collectNodes(
  SchemaNode root,
  Map<int, SchemaNode> nodeMap,
  List<SchemaNode> structs,
  List<SchemaNode> enums,
  List<SchemaNode> interfaces,
) {
  for (final nested in root.nestedNodes) {
    final node = nodeMap[nested.id];
    if (node == null) continue;
    if (node.body is StructBody) {
      final body = node.body as StructBody;
      if (!body.isGroup) structs.add(node);
      _collectNodes(node, nodeMap, structs, enums, interfaces);
    } else if (node.body is EnumBody) {
      enums.add(node);
    } else if (node.body is InterfaceBody) {
      interfaces.add(node);
      final iface = node.body as InterfaceBody;
      for (final method in iface.methods) {
        for (final id in [method.paramStructTypeId, method.resultStructTypeId]) {
          final ms = nodeMap[id];
          if (ms != null && ms.body is StructBody) {
            final sb = ms.body as StructBody;
            if (!sb.isGroup) structs.add(ms);
          }
        }
      }
    }
  }
}

void _collectGroups(
  SchemaNode node,
  Map<int, SchemaNode> nodeMap,
  List<SchemaNode> groups,
  Set<int> seen,
) {
  final body = node.body;
  if (body is! StructBody) return;
  for (final field in body.fields) {
    if (field.body is! GroupField) continue;
    final gf = field.body as GroupField;
    if (seen.contains(gf.typeId)) continue;
    final groupNode = nodeMap[gf.typeId];
    if (groupNode == null) continue;
    seen.add(gf.typeId);
    groups.add(groupNode);
    _collectGroups(groupNode, nodeMap, groups, seen);
  }
}

