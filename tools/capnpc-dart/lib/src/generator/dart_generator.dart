String _dartClassName(String rawName) {
  var name = rawName;
  final colonIdx = name.lastIndexOf(':');
  if (colonIdx >= 0) name = name.substring(colonIdx + 1);
  if (name.contains(r'$') && name.contains('.')) {
    name = name.split('.').map(_ucfirst).join();
  } else {
    final dotIdx = name.lastIndexOf('.');
    if (dotIdx >= 0) name = name.substring(dotIdx + 1);
  }
  if (name.contains(r'$')) {
    name = name.split(r'$').map(_ucfirst).join();
  }
  return name;
}

String _camel(String s) {
  if (s.isEmpty) return s;
  if (s[0] == s[0].toLowerCase()) return s;
  return s[0].toLowerCase() + s.substring(1);
}

String _lcfirst(String s) =>
    s.isEmpty ? s : s[0].toLowerCase() + s.substring(1);
String _ucfirst(String s) =>
    s.isEmpty ? s : s[0].toUpperCase() + s.substring(1);

String _hexId(int id) {
  final hi = id >>> 32;
  final lo = id & 0xFFFFFFFF;
  return '0x${hi.toRadixString(16).padLeft(8, '0')}${lo.toRadixString(16).padLeft(8, '0')}';
}
