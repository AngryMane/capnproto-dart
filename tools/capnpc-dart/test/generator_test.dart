  group('generateDartFile — interface', () {
    const paramsId = 30;
    const resultsId = 31;
    const ifaceId = 0x1234abcd; // positive id for easy hex check

    late String src;
    setUp(() {
      final paramsNode = structNode(paramsId, 'GreetParams', 0, 1, [
        ptrField('name', 0, 0, const TextType()),
      ]);
      final resultsNode = structNode(resultsId, 'GreetResults', 0, 1, [
        ptrField('reply', 0, 0, const TextType()),
      ]);
      final iface = interfaceNode(ifaceId, 'Greeter', [
        SchemaMethod(
          name: 'greet',
          ordinal: 0,
          paramStructTypeId: paramsId,
          resultStructTypeId: resultsId,
        ),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'Greeter', id: ifaceId)]);
      src = generateDartFile(file, [file, paramsNode, resultsNode, iface]);
    });

    test('imports capnproto_dart_rpc', () {
      expect(src, contains("import 'package:capnproto_dart_rpc/capnproto_dart_rpc.dart'"));
    });

    test('params and results structs appear before the client stub', () {
      final paramsIdx = src.indexOf('GreetParamsReader');
      final clientIdx = src.indexOf('GreeterClient');
      expect(paramsIdx, lessThan(clientIdx));
    });

    test('generates client class with correct interface id', () {
      expect(src, contains('class GreeterClient extends Capability'));
      expect(src, contains('static const int _interfaceId = 0x000000001234abcd'));
    });

    test('generates typed greet method', () {
      expect(src, contains('Future<GreetResultsReader> greet('));
      expect(src, contains('void Function(GreetParamsBuilder) build'));
      expect(src, contains('_cap.dispatch(0x000000001234abcd, 0, mb.serialize())'));
      expect(src, contains('.getRoot(greetResultsFactory)'));
    });

    test('generates factory', () {
      expect(src, contains('class GreeterClientFactory extends CapabilityFactory<GreeterClient>'));
      expect(src, contains('GreeterClient fromCapability(Capability cap)'));
    });
  });

  group('generateDartFile — explicit defaults', () {
    test('uint32 field with non-zero default emits defaultValue: arg', () {
      final sNode = structNode(20, 'S', 1, 0, [
        defaultedField('x', 0, 0, const UInt32Type(), 42),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'S', id: 20)]);
      final src = generateDartFile(file, [file, sNode]);

      expect(src, contains('getUint32Field(0, defaultValue: 42)'));
      expect(src, contains('setUint32Field(0, v, defaultValue: 42)'));
    });

    test('bool field with default true emits defaultValue: true', () {
      final sNode = structNode(20, 'S', 1, 0, [
        defaultedField('flag', 0, 0, const BoolType(), true),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'S', id: 20)]);
      final src = generateDartFile(file, [file, sNode]);

      expect(src, contains('getBoolField(0, defaultValue: true)'));
      expect(src, contains('setBoolField(0, v, defaultValue: true)'));
    });

    test('zero/false default does not emit defaultValue: arg', () {
      final sNode = structNode(20, 'S', 1, 0, [
        defaultedField('x', 0, 0, const UInt32Type(), 0),
        defaultedField('flag', 1, 1, const BoolType(), false),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'S', id: 20)]);
      final src = generateDartFile(file, [file, sNode]);

      expect(src, isNot(contains('defaultValue:')));
    });

    test('null defaultValue does not emit defaultValue: arg', () {
      final sNode = structNode(20, 'S', 1, 0, [
        SchemaField(
