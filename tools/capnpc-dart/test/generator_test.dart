    SchemaNode keyValueTemplate() => structNode(
          100,
          'KeyValue',
          0,
          2,
          [
            ptrField('key', 0, 0, const TypeParameterRefType(0)),
            ptrField('value', 1, 1, const TypeParameterRefType(1)),
          ],
