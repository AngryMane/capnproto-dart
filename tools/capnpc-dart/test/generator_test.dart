  group('generateDartFile — enum ref field', () {
    test('generates fromUint16 getter and toUint16 setter', () {
      const colorId = 50;
      final colorNode = enumNode(colorId, 'Color', [
        const SchemaEnumerant(name: 'red', codeOrder: 0),
