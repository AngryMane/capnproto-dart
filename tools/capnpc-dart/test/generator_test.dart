void main() {
  group('generateDartFile — empty file', () {
    test('produces header and no class bodies', () {
      final file = fileNode(1, []);
      final src = generateDartFile(file, [file]);
      expect(src, contains("import 'package:capnproto_dart/capnproto_dart.dart'"));
      expect(src, contains('// Generated by capnpc-dart'));
      expect(src, isNot(contains('class ')));
    });
  });

  group('generateDartFile — enum', () {
    test('generates enum values and converters', () {
      final eNode = enumNode(10, 'Color', [
        const SchemaEnumerant(name: 'red', codeOrder: 0),
        const SchemaEnumerant(name: 'green', codeOrder: 1),
        const SchemaEnumerant(name: 'blue', codeOrder: 2),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'Color', id: 10)]);
      final src = generateDartFile(file, [file, eNode]);

      expect(src, contains('enum Color {'));
      expect(src, contains('red,'));
      expect(src, contains('green,'));
      expect(src, contains('blue,'));
      expect(src, contains('Color? colorFromUint16(int v)'));
      expect(src, contains('int colorToUint16(Color v)'));
    });

    test('sorts enumerants by codeOrder', () {
      final eNode = enumNode(10, 'Priority', [
        const SchemaEnumerant(name: 'low', codeOrder: 2),
        const SchemaEnumerant(name: 'high', codeOrder: 0),
        const SchemaEnumerant(name: 'medium', codeOrder: 1),
      ]);
      final file = fileNode(1, [SchemaNestedNode(name: 'Priority', id: 10)]);
      final src = generateDartFile(file, [file, eNode]);

      final highIdx = src.indexOf('high,');
      final medIdx = src.indexOf('medium,');
      final lowIdx = src.indexOf('low,');
      expect(highIdx, lessThan(medIdx));
      expect(medIdx, lessThan(lowIdx));
    });
  });

  group('generateDartFile — simple struct', () {
    late String src;

    setUp(() {
      final sNode = structNode(
        20,
        'Person',
        1, // 1 data word
        1, // 1 pointer
        [
          dataField('age', 0, 0, const UInt32Type()),  // byte offset 0
          ptrField('name', 1, 0, const TextType()),    // ptr slot 0
        ],
      );
      final file = fileNode(1, [SchemaNestedNode(name: 'Person', id: 20)]);
      src = generateDartFile(file, [file, sNode]);
    });

    test('generates Reader class', () {
      expect(src, contains('final class PersonReader extends StructReader'));
      expect(src, contains('int get age => getUint32Field(0)'));
      expect(src, contains("String? get name => getTextField(0)"));
    });

    test('generates Builder class', () {
      expect(src, contains('final class PersonBuilder extends StructBuilder'));
      expect(src, contains('set age(int v)'));
      expect(src, contains('setUint32Field(0, v)'));
      expect(src, contains('set name(String? v)'));
      expect(src, contains('setTextField(0, v)'));
    });

    test('generates Factory class with correct dims', () {
      expect(src, contains('final class _PersonFactory'));
      expect(src, contains('int get dataWords => 1'));
      expect(src, contains('int get ptrWords => 1'));
      expect(src, contains('PersonReader fromRawReader'));
      expect(src, contains('PersonBuilder fromRawBuilder'));
    });

    test('generates factory singleton', () {
      expect(src, contains('final personFactory = _PersonFactory()'));
    });
  });

