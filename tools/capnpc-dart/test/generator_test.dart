import 'package:capnpc_dart/capnpc_dart.dart';
import 'package:test/test.dart';

// ---------------------------------------------------------------------------
// Helpers to build in-memory schema models.
// ---------------------------------------------------------------------------

SchemaNode fileNode(int id, List<SchemaNestedNode> nested) => SchemaNode(
      id: id,
      displayName: 'test.capnp',
      displayNamePrefixLength: 0,
      scopeId: 0,
      nestedNodes: nested,
      body: const FileBody(),
    );

SchemaNode structNode(
  int id,
  String name,
  int dw,
  int pw,
  List<SchemaField> fields, {
  bool isGroup = false,
  int discCount = 0,
  int discOffset = 0,
  List<SchemaNestedNode> nested = const [],
}) =>
    SchemaNode(
      id: id,
      displayName: 'test.capnp:$name',
      displayNamePrefixLength: 'test.capnp:'.length,
      scopeId: 1,
      nestedNodes: nested,
      body: StructBody(
        dataWordCount: dw,
        pointerCount: pw,
        isGroup: isGroup,
        discriminantCount: discCount,
        discriminantOffset: discOffset,
        fields: fields,
      ),
    );

SchemaNode interfaceNode(int id, String name, List<SchemaMethod> methods) =>
    SchemaNode(
      id: id,
      displayName: 'test.capnp:$name',
      displayNamePrefixLength: 'test.capnp:'.length,
      scopeId: 1,
      nestedNodes: const [],
      body: InterfaceBody(methods: methods),
    );

SchemaNode enumNode(int id, String name, List<SchemaEnumerant> enumerants) =>
    SchemaNode(
      id: id,
      displayName: 'test.capnp:$name',
      displayNamePrefixLength: 'test.capnp:'.length,
      scopeId: 1,
      nestedNodes: const [],
      body: EnumBody(enumerants: enumerants),
    );

SchemaField dataField(String name, int codeOrder, int offset, SchemaType type) =>
    SchemaField(
      name: name,
      codeOrder: codeOrder,
      discriminantValue: 0xFFFF,
      body: SlotField(offset: offset, type: type, hadExplicitDefault: false),
    );

SchemaField unionField(
        String name, int codeOrder, int discVal, int offset, SchemaType type) =>
    SchemaField(
      name: name,
      codeOrder: codeOrder,
      discriminantValue: discVal,
      body: SlotField(offset: offset, type: type, hadExplicitDefault: false),
    );

SchemaField ptrField(String name, int codeOrder, int offset, SchemaType type) =>
    SchemaField(
      name: name,
      codeOrder: codeOrder,
      discriminantValue: 0xFFFF,
      body: SlotField(offset: offset, type: type, hadExplicitDefault: false),
    );

SchemaField defaultedField(
        String name, int codeOrder, int offset, SchemaType type,
        Object? defaultValue) =>
    SchemaField(
      name: name,
      codeOrder: codeOrder,
      discriminantValue: 0xFFFF,
      body: SlotField(
          offset: offset,
          type: type,
          hadExplicitDefault: true,
          defaultValue: defaultValue),
    );

